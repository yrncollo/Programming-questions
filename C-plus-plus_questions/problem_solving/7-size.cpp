#include <iostream>

int main()
{
	double a;
	int b;
	float c;
	char d;

	std::cout << "The size of double is: " << sizeof(a) << "\n";
	std::cout << "The size of int is: " << sizeof(b)<< "\n";
	std::cout << "The size of float is: " << sizeof(c)<< "\n";
	std::cout << "The size of char is: " << sizeof(d)<< "\n";
}
